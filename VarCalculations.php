                <h2>About</h2>
                <p>Current post-fire assessment procedures require identification and valuation of
                    values-at-risk (VAR), such as human life and safety, cultural and ecological
                    resources, land use, and existing infrastructure, from potential secondary
                    effects (e.g., increased runoff, erosion, flooding, sedimentation, and vulnerability
                    to invasive weeds) of wildfires. However, the valuation of VAR, especially non-market
                    resource values, is a complex process that exceeds the time and expertise available
                    to most BAER/ESR. </p><br>
                <p>We developed a method (framework) to standardize and simplify the complex valuation
                    task in the context of post-fire assessment and treatment recommendation. The
                    spreadsheet-based VAR Calculation Tool supports this VAR valuation framework. </p><br>
                <p>The VAR Tool LITE spreadsheet simplifies the input requirements, removes the GIS
                    map requirements, streamlines the terminology and instructions compared to the
                    original calculation tool.</p><br>
                <p>The use of the VAR Calculation Tool to support post-fire values-at-risk assessment is described in
                    <a href="/mfsl/downloads/rmrs_gtr205.pdf" target="_blank">Assessing Post-Fire Values-at-Risk with a New Calculation Tool</a>
